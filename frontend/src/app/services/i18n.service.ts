@Injectable({ providedIn: 'root' })
export class I18nService {
  private currentLang = 'en';
  private lang$ = new BehaviorSubject<string>('en');
  langChange$ = this.lang$.asObservable();

  constructor() {
    // Detect browser language as initial default
    const browserLang = (navigator.language || '').substring(0, 2).toLowerCase();
    const supported = EU_LANGUAGES.map(l => l.code);
    if (supported.includes(browserLang)) {
      this.currentLang = browserLang;
    }

    // Check localStorage override
    const stored = localStorage.getItem('app_language');
    if (stored && supported.includes(stored)) {
      this.currentLang = stored;
    }

    this.lang$.next(this.currentLang);
  }

  get lang(): string {
    return this.currentLang;
  }

  setLanguage(code: string): void {
    this.currentLang = code;
    localStorage.setItem('app_language', code);
    this.lang$.next(code);
  }

  /** Initialize from user profile (called after login) */
  initFromUser(userLang: string): void {
    if (userLang && EU_LANGUAGES.some(l => l.code === userLang)) {
      this.currentLang = userLang;
      localStorage.setItem('app_language', userLang);
      this.lang$.next(userLang);
    }
  }

  /** Translate a key. Falls back to English, then returns the key itself. */
  t(key: string): string {
    const langDict = TRANSLATIONS[this.currentLang];
    if (langDict && langDict[key]) return langDict[key];
    // Fallback to English
    const enDict = TRANSLATIONS['en'];
    if (enDict && enDict[key]) return enDict[key];
    return key;
  }
}
